  <h1>Receita de Torta de Limão</h1>

  <p>Refrescante, cremosa e com aquele toque azedinho irresistível! <br>A torta de limão é perfeita para dias quentes ou para impressionar em qualquer ocasião. E o melhor: é fácil de fazer!</p>

  <img src="https://www.receiteria.com.br/wp-content/uploads/torta-de-limao-facil-capa.jpeg" alt="torta de limão" width="400">

  <h2>Ingredientes</h2>
  <ul>
    <li>1 pacote de biscoito maisena (cerca de 200g)</li>
    <li>100g de manteiga derretida</li>
    <li>1 lata de leite condensado</li>
    <li>1 caixinha de creme de leite</li>
    <li>Suco de 3 limões</li>
    <li>Raspas de limão para decorar</li>
  </ul>

  <h2>Modo de Preparo</h2>
  <ol>
    <li>Triture os biscoitos maisena no liquidificador ou processador até virar uma farofa fina.</li>
    <li>Misture a farofa com a manteiga derretida até formar uma massa úmida.</li>
    <li>Forre o fundo e as laterais de uma forma com essa massa e leve ao forno por 10 minutos a 180°C. Reserve.</li>
    <li>Em uma tigela, misture o leite condensado, o creme de leite e o suco dos limões até ficar bem homogêneo.</li>
    <li>Despeje esse creme sobre a massa já fria e leve à geladeira por pelo menos 4 horas.</li>
  </ol>
  


